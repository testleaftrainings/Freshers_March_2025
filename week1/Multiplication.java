package week1;

public class Multiplication {

	public static void main(String[] args) {

		// 1 * 5 = 5

		// initialize the value

		int num = 16;

		// iterate the values upto 10

		for (int i = 1; i <= 10; i++) {

			System.out.println(i + " * " + num + " = " + i * num);

		}

	}
}
