package week1;

public class LearnIfElse {

	public static void main(String[] args) {


		int mark=30;
		
		if (mark>=90) {
			System.out.println("good");
		}
		else if (mark<=50 || mark>30) {
			System.out.println("Average");
		}
		
		else {
			System.out.println("Improve yourself");
		}
		
	
	}

}
